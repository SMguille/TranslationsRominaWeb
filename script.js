document.addEventListener('DOMContentLoaded', () => {
    const languageToggle = document.getElementById('language-toggle');
    let currentLang = 'en'; // Default language

    languageToggle.addEventListener('click', () => {
        currentLang = currentLang === 'en' ? 'es' : 'en';
        updateLanguage(currentLang);
        languageToggle.textContent = currentLang === 'en' ? 'ES' : 'EN';
    });

    // Call updateLanguage on page load to set the initial language
    updateLanguage(currentLang);
});

// Update language
function updateLanguage(lang) {
    document.querySelectorAll('[data-lang]').forEach(element => {
        const key = element.getAttribute('data-lang');
        if (translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                // Update placeholder for inputs and textareas
                element.placeholder = translations[lang][key];
            } else {
                // Update innerHTML for other elements
                element.innerHTML = translations[lang][key];
            }
        }
    });
}

// Select the hamburger button and the nav menu
const hamburger = document.querySelector('.hamburger');
const navMenu = document.querySelector('.nav-menu');

// Add a click event listener to the hamburger button
hamburger.addEventListener('click', () => {
    // Toggle the "active" class on the nav menu
    navMenu.classList.toggle('active');

    // Optional: Toggle the hamburger icon animation
    hamburger.classList.toggle('is-active');
});

// File upload name display
document.getElementById('fileUpload').addEventListener('change', function(e) {
    const fileName = e.target.files[0]?.name || 'No file chosen';
    document.querySelector('.file-upload-text').textContent = fileName;
});

// Smooth scrolling for anchor links
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function(e) {
        e.preventDefault();
        document.querySelector(this.getAttribute('href')).scrollIntoView({
            behavior: 'smooth'
        });
    });
});

// Animate elements on scroll
const animateOnScroll = () => {
    const elements = document.querySelectorAll('.animated');
    elements.forEach(element => {
        const elementTop = element.getBoundingClientRect().top;
        const elementBottom = element.getBoundingClientRect().bottom;
        const isVisible = (elementTop < window.innerHeight) && (elementBottom >= 0);
        if (isVisible) {
            element.classList.add('fadeInUp');
        }
    });
};

window.addEventListener('scroll', animateOnScroll);
animateOnScroll(); // Trigger on page load